    void LlenarComboboxTipoUsuario() {

        try {
            Conn = clsConecta.getConnection();
            String sql = "SELECT id_usuario,cargo FROM usuario ORDER BY id_usuario";
            sent = Conn.createStatement();
            ResultSet rs = sent.executeQuery(sql);
            modeloCboTipoUsuario.addElement("[ Elije Tipo Usuario ]");
            while (rs.next()) {
                modeloCboTipoUsuario.addElement(rs.getString("cargo"));
                cbo_tipoUsuario.setModel(modeloCboTipoUsuario);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

    public int validar_ingreso() {

        String cargo = cbo_tipoUsuario.getSelectedItem().toString();
        String clave = String.valueOf(txt_Contrasena.getPassword());

        int resultado = 0;

        String SSQL = "SELECT id_usuario FROM usuario WHERE cargo='" + cargo + "' AND clave_usuario = '" + clave + "' ";
        try {

            Conn = clsConecta.getConnection();
            Statement st = Conn.createStatement();
            ResultSet rs = st.executeQuery(SSQL);

            if (!rs.next()) {

                return 0;

            }
            return Integer.parseInt(rs.getString("id_usuario"));

        } catch (SQLException ex) {

            JOptionPane.showMessageDialog(null, ex, "Error de conexión", JOptionPane.ERROR_MESSAGE);

        } finally {

            try {

                Conn.close();

            } catch (SQLException ex) {

                JOptionPane.showMessageDialog(null, ex, "Error de desconexión", JOptionPane.ERROR_MESSAGE);

            }

        }

        return resultado;

    }

